public class Calculator {

    public Calculator(){}

    public int add(int one, int two){
        return one + two;
    }

    public int subtract(int one, int two){
        return one - two;
    }

    public int multiply(int one, int two){
        return one * two;
    }

    public double divide(double one, double two){
        return one / two;
    }

}